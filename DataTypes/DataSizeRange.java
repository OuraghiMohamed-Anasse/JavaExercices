import java.lang.*;

public class DataSizeRange

{
	public static void main(String args[])
	{

		System.out.println("Valeur min des int : " +Integer.MIN_VALUE);
		System.out.println("Valeur max des int : "+Integer.MAX_VALUE);
		System.out.println("Valeur des bytes : "+Integer.BYTES);
		System.out.println("Valeur min des float : "+Float.MIN_VALUE);
		System.out.println("Valeur max des float : "+ Float.MAX_VALUE);
		System.out.println("valeur des bytes pour les float :" + Float.BYTES);
		System.out.println("valeur min des doubles : "  + Double.MIN_VALUE);
		System.out.println("valeur max des doubles : "  + Double.MAX_VALUE);
	}
}	